# For loops in python -
# 1. Many objects in Python are "iterable", meaning we can iterate over every element in the object.
#    Such as every element in a list or every character in a string.
# 2. We can use for loops to execute a block of code for every iteration.
# 3. The term "iterable" means you can "iterate" over the object.
#    For example you can iterate over every character in a string, iterate over every item in a list, iterate over every
#    key in a dictionary.

# Syntax -
#    my_iterable = [1,2,3]
#    for item_name in my_iterable:
#      print(item_name)
#
#   O/P - 1
#         2
#         3

# While loops in python -
# 1. while loops will continue to execute a block of code while some condition remains True.
#    For Example - while my pool is not full, keep filling my pool with water.
#                        OR
#                  while my dogs are still hungry, keep feeding my dogs.
#
#    Syntax -
#       while some_boolean_condition:
#           # do something
#        else:
#            # do something else

# break, continue, pass
# 1. We can use break, continue, and pass statements in our loops to add additional functionality forvarious cases.
#    The three statements are defined by:
#         break: Breaks out of the current closest enclosing loop.
#         continue: Goes to the top of the closest enclosing loop.
#         pass: Does nothing at all.

